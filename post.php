                        <h1 class="entry-title">
                            孙子兵法·计篇
                        </h1>
                        <div class="entry-content">孙子曰:兵者，国之大事，死生之地，存亡之道，不可不察也。<br>
                            
            故经之以五事，校之以计，而索其情:一曰道，二曰天，三曰地，四曰将，五曰法。<br>

            道者，令民与上同意，可与之死，可与之生，而不危也；天者，阴阳、寒暑、时制也；地者，远近、险易、广狭、死生也；将者，智、信、仁、勇、严也；法者，曲制、官道、主用也。<br>

            凡此五者，将莫不闻，知之者胜，不知之者不胜。<br>
                            </p>
                            
                            <p class="wrap-blockquote">

            故校之以计，而索其情，曰:主孰有道？<br>

            将孰有能？<br>

            天地孰得？<br>

            法令孰行？<br>

            兵众孰强？<br>

            士卒孰练？<br>

            赏罚孰明？<br>

            吾以此知胜负矣。<br>

            将听吾计，用之必胜，留之；将不听吾计，用之必败，去之。<br>

            计利以听，乃为之势，以佐其外。<br>

            势者，因利而制权也。<br>

            兵者，诡道也。<br>

            故能而示之不能，用而示之不用，近而示之远，远而示之近。<br>

            利而诱之，乱而取之，实而备之，强而避之，怒而挠之，卑而骄之，佚而劳之，亲而离之，攻其无备，出其不意。<br>

            此兵家之胜，不可先传也。<br>

            夫未战而庙算胜者，得算多也；未战而庙算不胜者，得算少也。<br>

            多算胜少算，而况于无算乎!<br>

            吾以此观之，胜负见矣。</p>
